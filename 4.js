function sumarProductos(precioUnitario, cantidadDeseada) {
    // Calcular el total gastado
    let totalGastado = precioUnitario * cantidadDeseada;
    
    // Mostrar el resultado en consola
    console.log(`Total gastado: ${totalGastado} unidades monetarias`);
}
// Ejemplo de uso de la función sumarProductos
sumarProductos(10, 5); // Esto imprimirá: Total gastado: 50 unidades monetarias
